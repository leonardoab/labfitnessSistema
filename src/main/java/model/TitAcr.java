package model;

public class TitAcr {
	
	private String codEstab;
	private String codEspecDocto;
	private String codSerDocto;
	private String codParcela;
	private int cdnCliente;
	private String codTitAcr;
		
	public String getCodEstab() {
		return codEstab;
	}
	public void setCodEstab(String codEstab) {
		this.codEstab = codEstab;
	}
	public String getCodEspecDocto() {
		return codEspecDocto;
	}
	public void setCodEspecDocto(String codEspecDocto) {
		this.codEspecDocto = codEspecDocto;
	}
	public String getCodSerDocto() {
		return codSerDocto;
	}
	public void setCodSerDocto(String codSerDocto) {
		this.codSerDocto = codSerDocto;
	}
	public String getCodParcela() {
		return codParcela;
	}
	public void setCodParcela(String codParcela) {
		this.codParcela = codParcela;
	}
	
	public void setCodTitAcr(String codTitAcr) {
		this.codTitAcr = codTitAcr;
	}
	
	public String getCodTitAcr() {
		return codTitAcr;
	}
	public int getCdnCliente() {
		return cdnCliente;
	}
	public void setCdnCliente(int cdnCliente) {
		this.cdnCliente = cdnCliente;
	}
	
}
